package hotel.entity;

public class RoomCategory {
    
    private int id;
    private int price;
    
    public RoomCategory() {
        
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }
    
}
